package com.util.model;

public class RelacaoDias {
	
	private int segundasFeitas;
	private int tercasFeitas;
	private int quartasFeitas;
	private int quintasFeitas;
	private int sextasFeitas;
	private int sabadosFeitas;
	private int diasCompletos;
	private int diasNaoFeitos;
	
	public int getSegundasFeitas() {
		return segundasFeitas;
	}
	public void setSegundasFeitas(int segundasFeitas) {
		this.segundasFeitas = segundasFeitas;
	}
	public int getTercasFeitas() {
		return tercasFeitas;
	}
	public void setTercasFeitas(int tercasFeitas) {
		this.tercasFeitas = tercasFeitas;
	}
	public int getQuartasFeitas() {
		return quartasFeitas;
	}
	public void setQuartasFeitas(int quartasFeitas) {
		this.quartasFeitas = quartasFeitas;
	}
	public int getQuintasFeitas() {
		return quintasFeitas;
	}
	public void setQuintasFeitas(int quintasFeitas) {
		this.quintasFeitas = quintasFeitas;
	}
	public int getSextasFeitas() {
		return sextasFeitas;
	}
	public void setSextasFeitas(int sextasFeitas) {
		this.sextasFeitas = sextasFeitas;
	}
	public int getSabadosFeitas() {
		return sabadosFeitas;
	}
	public void setSabadosFeitas(int sabadosFeitas) {
		this.sabadosFeitas = sabadosFeitas;
	}
	public int getDiasCompletos() {
		return diasCompletos;
	}
	public void setDiasCompletos(int diasCompletos) {
		this.diasCompletos = diasCompletos;
	}
	public int getDiasNaoFeitos() {
		return diasNaoFeitos;
	}
	public void setDiasNaoFeitos(int diasNaoFeitos) {
		this.diasNaoFeitos = diasNaoFeitos;
	}
}
