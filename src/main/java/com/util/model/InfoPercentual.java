package com.util.model;

public class InfoPercentual {
	
	private String promoter;
	private Long countActivities;
	private Long countActivitiesDone;
	private Long percetual;
	
	public String getPromoter() {
		return promoter;
	}
	public void setPromoter(String promoter) {
		this.promoter = promoter;
	}
	public Long getCountActivities() {
		return countActivities;
	}
	public void setCountActivities(Long countActivities) {
		this.countActivities = countActivities;
	}
	public Long getCountActivitiesDone() {
		return countActivitiesDone;
	}
	public void setCountActivitiesDone(Long countActivitiesDone) {
		this.countActivitiesDone = countActivitiesDone;
	}
	public Long getPercetual() {
		return percetual;
	}
	public void setPercetual(Long percetual) {
		this.percetual = percetual;
	}
}
